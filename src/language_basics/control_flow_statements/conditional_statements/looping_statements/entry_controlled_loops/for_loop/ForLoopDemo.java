package src.language_basics.control_flow_statements.conditional_statements.looping_statements.entry_controlled_loops.for_loop;

public class ForLoopDemo {

    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.printf("5 X %d = %d%n", i, 5 * i);
        }
    }

}
