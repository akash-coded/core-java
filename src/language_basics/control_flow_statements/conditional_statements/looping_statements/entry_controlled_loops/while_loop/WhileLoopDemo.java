package src.language_basics.control_flow_statements.conditional_statements.looping_statements.entry_controlled_loops.while_loop;

public class WhileLoopDemo {

    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.printf("5 X %d = %d%n", i, 5 * i);
            i++;
        }
    }

}
