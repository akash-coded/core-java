package src.language_basics.operators.arithmetic_operators;

public class ArithmeticOperators {

    public static void main(String[] args) {

        // declare variables
        int a = 12;
        int b = 5;

        // addition operator
        System.out.println("a + b = " + (a + b));

        // subtraction operator
        System.out.println("a - b = " + (a - b));

        // multiplication operator
        System.out.println("a * b = " + (a * b));

        // division operator
        System.out.println("a / b = " + (a / b));

        // modulo operator
        System.out.println("a % b = " + (a % b));

    }

}
